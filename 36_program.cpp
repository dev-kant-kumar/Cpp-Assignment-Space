// 36. Write a program in C++ to test Type Casting.

#include <iostream>
using namespace std;

int main()
{

    cout << "Implicit Typecasting " << "10/7 =" << 10 / 7;
    cout << "\nImplicit Typecasting " << "10.0/7 =" << 10.0 / 7;
    cout << "\nExplicit Typecasting " << "10/7 =" << (float)(10) / 7;

    return 0;
}
